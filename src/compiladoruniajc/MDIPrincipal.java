  public void logueo()
 {
     
        
         
                 
         int level=0;
         String usuario=txt_usuario.getText();
         String contra=String.valueOf(pf_contra.getPassword());
         
         if(us.equals(usuario) && con.equals(contra))
         {
             JDInicio.dispose();
             txt_usuario.setText("");
             pf_contra.setText("");
             
             lb_us.setText(usuario);
             
             this.setVisible(true);
         }
         
            else
         {
             JOptionPane.showMessageDialog(this, "Usuario o contrase√±a incorrectos. \n Intentelo de nuevo", "Mensaje Error", JOptionPane.ERROR_MESSAGE);
         }
 }

